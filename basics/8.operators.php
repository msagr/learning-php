<?php
    /* Operators

        *   Arithmetic Operators :-

        1. Addition             -       +
        2. Substraction         -       -
        3. Multiplication       -       *
        4. Division             -       /
        5. Modulus              -       %
        6. Concatenation        -       . (for strings)

        *   Assignment Operators :-

        1. $x = $y
        2. $x += $y   --->    $x = $x + $y
        3. $x -= $y   --->    $x = $x - $y
        4. $x *= $y   --->    $x = $x * $y
        5. $x /= $y   --->    $x = $x / $y
        6. $x %= $y   --->    $x = $x % $y
        7. $x .= $y   --->    $x = $x . $y

        *   Increment/Decrement Operators :-

        1. ++$x     --->        Pre-increment
        2. $x++     --->        Post-increment
        3. --$x     --->        Pre-decrement
        4. $x--     --->        Post-decrement

        *  Comparison Operators :-

        1. ==       --->        Equal
        2. ===      --->        Identical
        3. !=       --->        Not equal
        4. <>       --->        Not equal
        5. !==      --->        Not identical

        *   Logical Operators :-

        1. and (&&)    --->        $x and $y  /  $x && $y
        2. or  (||)    --->        $x or $y   /  $x || $y
        3. xor         --->        $x xor $y
        4. !           --->        !$x

        *   Array Operators :-

        1. +           --->         $x + $y     (Union Operator)
        2. ==          --->         $x == $y    (Equality Operator)
        3. ===         --->         $x === $y   (Identity Operator)     
    */

    // Arithmetics

    $x = 10;
    $y = 6;

    echo ($x + $y);
    echo "<br>";
    echo ($x - $y);
    echo "<br>";
    echo ($x * $y);
    echo "<br>";
    echo ($x / $y);
    echo "<br>";
    echo ($x % $y);




?>